function U = interpAdj(V)
    globals; 
    U = zeros(N+2,P+2,Q+2,3);

    U(1    ,1:P+1,1:Q+1,1) = V(1,:,:,1);
    U(2:N+1,1:P+1,1:Q+1,1) = V(2:N+1,:,:,1) + V(1:N,:,:,1);
    U(N+2  ,1:P+1,1:Q+1,1) = V(N+1,:,:,1);
    
    U(1:N+1,1    ,1:Q+1,2) = V(:,1,:,2);
    U(1:N+1,2:P+1,1:Q+1,2) = V(:,2:P+1,:,2) + V(:,1:P,:,2);
    U(1:N+1,P+2  ,1:Q+1,2) = V(:,P+1,:,2);

    U(1:N+1,1:P+1,1    ,3) = V(:,:,1,3);
    U(1:N+1,1:P+1,2:Q+1,3) = V(:,:,2:Q+1,3) + V(:,:,1:Q,3);
    U(1:N+1,1:P+1,Q+2  ,3) = V(:,:,Q+1,3);
    
    U = 0.5*U;
end

